export function getTempLogShare(id: string | undefined): TempLogShare | undefined {
  if (!id) return undefined;
  return tempLogShares[id];
}

export function getTempLogShareHeaders(share?: TempLogShare): HeadersInit {
  return {
    'cache-control': 'no-store, max-age=0',
    'x-content-type-options': 'nosniff',
    'x-robots-tag': 'noindex, nofollow, noarchive',
    ...(share ? { expires: new Date(share.expiresAt).toUTCString() } : {}),
  };
}

export function isTempLogShareExpired(share: TempLogShare): boolean {
  const expiresAt = new Date(share.expiresAt).getTime();
  return Number.isNaN(expiresAt) || Date.now() > expiresAt;
}
